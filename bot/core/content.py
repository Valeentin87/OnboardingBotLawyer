def format_block1_section1_question(q_index: int, question_data: dict) -> str:
    """
    Формирует текст вопроса с вариантами ответа для Раздела №1.
    q_index — номер (0-based).
    """
    q_num = q_index + 1
    lines = [
        f"📝 **Вопрос №{q_num}**",
        "",
        question_data["question"],
        "",
    ]
    for opt in question_data["options"]:
        lines.append(opt)
    return "\n".join(lines)

def format_block1_section1_result(questions: list[dict], answers: list[str]) -> str:
    total = len(questions)
    correct_count = 0
    mistakes = []

    for i, q in enumerate(questions):
        user_ans = answers[i] if i < len(answers) else "Нет ответа"
        if user_ans == q["correct"]:
            correct_count += 1
        else:
            # Полный текст вариантов
            user_option = "Нет ответа"
            if user_ans in ("A", "B", "C", "D"):
                for opt in q["options"]:
                    if opt.startswith(user_ans):
                        user_option = opt
                        break
            correct_option = next(opt for opt in q["options"] if opt.startswith(q["correct"]))
            mistakes.append(
                {
                    "number": i + 1,
                    "question": q["question"],
                    "user_answer": user_option,
                    "correct_answer": correct_option,
                }
            )

    lines = [
        "📊 **Результаты теста по Разделу №1**",
        "",
        f"✅ Правильных ответов: **{correct_count} из {total}**",
        "",
    ]

    if correct_count == total:
        lines.append("🎉 **Отлично!** Все ответы верны — так держать! 💪")
    else:
        lines.append("❌ **Есть ошибки в ответах.** Обратите внимание на эти вопросы и повторите материал.\n")
        for m in mistakes:
            lines.append(f"**{m['question']}**")
            lines.append("")
            lines.append(f"Ваш ответ: {m['user_answer']}")
            lines.append(f"Правильный ответ: {m['correct_answer']}")
            lines.append("")
            lines.append("───────────────────────────")
            lines.append("")

    return "\n".join(lines)


